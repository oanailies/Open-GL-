const float animationDuration = 45.5f; 

void animatie_start()
{
    static float startTime = glfwGetTime();
    float currentTime = glfwGetTime();
    float elapsedTime = currentTime - startTime;
    float angularSpeedIncreased = angularSpeed * 8.0f;
    if (elapsedTime < animationDuration) {
        
        float lateralStartX = centerX + circleRadius * 2;
        float lateralStartZ = centerZ;
        float approachProgress = elapsedTime / animationDuration * 2.0f;
        float camX = lateralStartX - (circleRadius * 2 * approachProgress);
        float camZ = lateralStartZ;
        float camY = 120.0f - (elapsedTime / animationDuration) * 60.0f;

        myCamera.setPosition(glm::vec3(camX, camY, camZ));
        myCamera.setTarget(glm::vec3(centerX, 0.0f, centerZ));

        float yaw = glm::degrees(atan2(centerZ - camZ, centerX - camX));
        myCamera.setYaw(yaw);
        myCamera.setPitch(-30.0f);

        view = myCamera.getViewMatrix();
        myBasicShader.useShaderProgram();
        glUniformMatrix4fv(viewLoc, 1, GL_FALSE, glm::value_ptr(view));

        normalMatrix = glm::mat3(glm::inverseTranspose(view * model));
        glUniformMatrix3fv(normalMatrixLoc, 1, GL_FALSE, glm::value_ptr(normalMatrix));
    }
    else {
        myCamera.setPosition(pisiciPosition);
        view = myCamera.getViewMatrix();
        myBasicShader.useShaderProgram();
        glUniformMatrix4fv(viewLoc, 1, GL_FALSE, glm::value_ptr(view));
        normalMatrix = glm::mat3(glm::inverseTranspose(view * model));
        glUniformMatrix3fv(normalMatrixLoc, 1, GL_FALSE, glm::value_ptr(normalMatrix));
    }
}




void keyboardCallback(GLFWwindow* window, int key, int scancode, int action, int mode) {
	if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS) {
        glfwSetWindowShouldClose(window, GL_TRUE);
    }

	if (key >= 0 && key < 1024) {
        if (action == GLFW_PRESS) {
            pressedKeys[key] = true;
        } else if (action == GLFW_RELEASE) {
            pressedKeys[key] = false;
        }
    }

    if (key == GLFW_KEY_ENTER  && action == GLFW_PRESS)
    {
        animatie_prezenare = !animatie_prezenare;
        if (animatie_prezenare == false)
        {
            myCamera.setSpeed(15.0f);
        }
    }
}

bool firstMouse = true;
float lastX = 400, lastY = 300;
float pitch = 0.0f, yaw = -90.0f;
void mouseCallback(GLFWwindow* window, double xpos, double ypos) {
    //TODO
    if (firstMouse)
    {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    float xoffset = xpos - lastX;
    float yoffset = lastY - ypos;
    lastX = xpos;
    lastY = ypos;

    float sensitivity = 0.05f;
    xoffset *= sensitivity;
    yoffset *= sensitivity;

    yaw += xoffset;
    pitch += yoffset;

    if (pitch > 89.0f)
        pitch = 89.0f;
    if (pitch < -89.0f)
        pitch = -89.0f;

    myCamera.rotate(pitch, yaw);
    myCamera.getViewMatrix();
    glUniformMatrix4fv(viewLoc, 1, GL_FALSE, glm::value_ptr(view));
    normalMatrix = glm::mat3(glm::inverseTranspose(view * model));
    glUniformMatrix3fv(normalMatrixLoc, 1, GL_FALSE, glm::value_ptr(normalMatrix));
    glUniform3fv(lightDirLoc, 1, glm::value_ptr(glm::inverseTranspose(glm::mat3(view)) * lightDir));
}


